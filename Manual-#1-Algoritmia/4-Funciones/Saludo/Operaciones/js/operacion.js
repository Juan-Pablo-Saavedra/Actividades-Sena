function suma(pnumerouno, pnumerodos){
    let numerouno = pnumerouno;
    let numerodos = pnumerodos;
    let suma

    suma = numerodos + numerouno;
    
    return suma;
}

function resta(pnumerouno, pnumerodos){
    let numerouno = pnumerouno;
    let numerodos = pnumerodos;
    let restar

    restar = numerodos - numerouno;
    
    return restar;
}

function multiplicacion(pnumerouno, pnumerodos){
    let numerouno = pnumerouno;
    let numerodos = pnumerodos;
    let multiplicar

    multiplicar = numerodos * numerouno;
    
    return multiplicar;
}

function division(pnumerouno, pnumerodos){
    let numerouno = pnumerouno;
    let numerodos = pnumerodos;
    let dividir

    dividir = numerodos / numerouno;
    
    return dividir;
}